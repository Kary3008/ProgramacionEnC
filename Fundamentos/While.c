#include<stdio.h>

void main(void){

	//primera secuencia
	int num=1;
	
	while(num<=5)
	{
		printf("%d\n",num);
		num++;	
	}
	
	//segunda secuencia
	int nu=1;
		
	while(nu<=20)
	{
		printf("%d\n",nu);
		num++;	
	}
	
}
